const a = 7 //o Simbolo de = não está para comparação, más para atribuição
let b = 3

b += a //atribuição aditiva -> atribuição juntamente com adição -> é o mesmo que b = b + a
console.log(b)

b -= 4 //b = b -4
console.log(b)

b *= 2 //b = b * 2
console.log(b)

b /= 2 //b = b / 2
console.log(b)

b %= 2 //b = b % 2 -> operação modular
console.log(b)